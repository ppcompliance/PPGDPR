	var h =0
	function showtext(){
	    var divset=document.getElementsByClassName("label_0");
		if(h==0){
			for(var i =0;i<divset.length;i++){
				divset[i].style.display="block";
				}
		}
		if(h==1){
			for(var i =0;i<divset.length;i++){
				divset[i].style.display="none";
				}
		}
		h++;
		if(h==2){
			h=0;
		}
		
	}
